package my.manager.movie;

public class MovieBean {
	private int mno;
	private String mname;
	private String poster;
	private String genre;
	private int running_time;
	private String mdate;
	private int mage;
	private String director;
	private String actor;
	private String country;
	private double reser_rate;
	private double star_rate;
	
	public int getMno() {
		return mno;
	}
	public void setMno(int mno) {
		this.mno = mno;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}
	public String getPoster() {
		return poster;
	}
	public void setPoster(String poster) {
		this.poster = poster;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public int getRunning_time() {
		return running_time;
	}
	public void setRunning_time(int running_time) {
		this.running_time = running_time;
	}
	public String getMdate() {
		return mdate;
	}
	public void setMdate(String mdate) {
		this.mdate = mdate;
	}
	public int getMage() {
		return mage;
	}
	public void setMage(int mage) {
		this.mage = mage;
	}
	public String getDirector() {
		return director;
	}
	public void setDirector(String director) {
		this.director = director;
	}
	public String getActor() {
		return actor;
	}
	public void setActor(String actor) {
		this.actor = actor;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public double getReser_rate() {
		return reser_rate;
	}
	public void setReser_rate(double reser_rate) {
		this.reser_rate = reser_rate;
	}
	public double getStar_rate() {
		return star_rate;
	}
	public void setStar_rate(double star_rate) {
		this.star_rate = star_rate;
	}
	
	
}
